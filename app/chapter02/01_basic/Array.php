<?php
// 配列の宣言
$array = [
    3 => '整数のキー 3', // 数値キー
    '3' => '文字列のキー \'3\'', // 文字列キー '3' (数値として扱われる)
    '03' => '文字列のキー \'03\'', // 文字列キー '03' (そのまま文字列として扱われる)
    9.5 => '浮動小数点キー 9.5', // 小数点のあるキー (整数9として扱われる)
    true => 'trueのキー', // boolean true (1として扱われる)
    false => 'falseのキー', // boolean false (0として扱われる)
    null => 'nullのキー' // null (空の文字列として扱われる)
];

// 配列の内容を表示
var_export($array);
/*
出力:
array (
  3 => '文字列のキー \'3\'',
  '03' => '文字列のキー \'03\'',
  9 => '浮動小数点キー 9.5',
  1 => 'trueのキー',
  0 => 'falseのキー',
  '' => 'nullのキー',
)
*/
